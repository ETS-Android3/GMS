package com.GMS.manager.models;

public class Employees {
    private String employeeName;
    private int employeeIcon;
    private String employeeCurrentState;
    private int employeeStateIcon;
    private String employeeType;
    private int employeeTypeIcon;

    public String getEmployeeName() {
        return employeeName;
    }

    public void setEmployeeName(String employeeName) {
        this.employeeName = employeeName;
    }

    public int getEmployeeIcon() {
        return employeeIcon;
    }

    public void setEmployeeIcon(int employeeIcon) {
        this.employeeIcon = employeeIcon;
    }

    public String getEmployeeCurrentState() {
        return employeeCurrentState;
    }

    public void setEmployeeCurrentState(String employeeCurrentState) {
        this.employeeCurrentState = employeeCurrentState;
    }

    public int getEmployeeStateIcon() {
        return employeeStateIcon;
    }

    public void setEmployeeStateIcon(int employeeStateIcon) {
        this.employeeStateIcon = employeeStateIcon;
    }

    public String getEmployeeType() {
        return employeeType;
    }

    public void setEmployeeType(String employeeType) {
        this.employeeType = employeeType;
    }

    public int getEmployeeTypeIcon() {
        return employeeTypeIcon;
    }

    public void setEmployeeTypeIcon(int employeeTypeIcon) {
        this.employeeTypeIcon = employeeTypeIcon;
    }
}
